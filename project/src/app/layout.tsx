import type { Metadata } from "next";
import { Cormorant_Garamond, DM_Sans, Geist_Mono } from "next/font/google";
import ToastProvider from "../components/ui/toastProvider";
import StoreProvider from "../components/ui/storeProvider";
import { SWRProvider } from "@/src/providers/SWRProvider";
import { getCurrentUser } from "../lib/action/user.action";
import "./globals.css";
import { callAction } from "@/src/lib/utils/callAction";


const dmSans = DM_Sans({
  variable: "--font-dm-sans",
  subsets: ["latin", "latin-ext"],
  weight: ["300", "400", "500", "600", "700"],
  display: "swap",
});


const cormorantGaramond = Cormorant_Garamond({
  variable: "--font-cormorant-garamond",
  subsets: ["latin", "latin-ext"],
  weight: ["300", "400", "500", "600", "700"],
  display: "swap",
});


const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
  display: "swap",
});


export const metadata: Metadata = {
 title: "ChamCham",
 description: "Generated by create next app",
 icons: {
    icon: "/logo.svg",
  },
};


export default async function RootLayout({
 children,
}: Readonly<{
 children: React.ReactNode;
}>) {
 // action-(check role layout tổng)
 const currentUser = await callAction(() => getCurrentUser(), "Không thể tải thông tin tài khoản. Vui lòng thử lại sau.");


 return (
   <html
     lang="en"
     className={`${dmSans.variable} ${cormorantGaramond.variable} ${geistMono.variable} h-full antialiased`}
   >
     <body
       className="min-h-full flex flex-col"
       data-role={currentUser?.role ?? "GUEST"}
       suppressHydrationWarning
     >
        {/* Inline script chạy đồng bộ trước khi browser paint đầu tiên */}
        {/* Nếu intro chưa được xem → set intro-playing ngay để tránh flash nội dung */}
        <script
          dangerouslySetInnerHTML={{
            __html: `(function(){
  try {
    if (sessionStorage.getItem('intro-shown') !== 'true') {
      document.body.classList.add('intro-playing');
    }
  } catch(e) {}
})();`,
          }}
        />
       <SWRProvider>
         <StoreProvider>
          <ToastProvider>
            {children}
          </ToastProvider>
        </StoreProvider>
       </SWRProvider>
     </body>
   </html>
 );
}


